T = int(input())
for _ in range(1, T + 1):
    N = int(input())
    mok = N // 6
    result = (6 * ((mok * (mok + 1)) // 2)) + ((N % 6) * (mok + 1))
    if N % 6 == 0:
        result += 1
        print(result)
    else:
        print(result)

